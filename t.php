function byte8($veri) {
    return utf8_encode($veri);
}

function rc2_sifrele($str_giris) {
    $ary_key = byte8("12341234");  // Key 8 bayt olmalı
    $ary_iv = byte8("12341234");   // IV de 8 bayt olmalı
    $pad_len = 8 - (strlen($str_giris) % 8);
    $str_giris = $str_giris . str_repeat(chr($pad_len), $pad_len);  // PKCS5 padding

    $encrypted = openssl_encrypt($str_giris, 'RC2-CBC', $ary_key, OPENSSL_RAW_DATA | OPENSSL_ZERO_PADDING, $ary_iv);
    return base64_encode($encrypted);
}

function rc2_coz($str_giris) {
    $ary_key = byte8("12341234");
    $ary_iv = byte8("12341234");

    $decrypted = openssl_decrypt(base64_decode($str_giris), 'RC2-CBC', $ary_key, OPENSSL_RAW_DATA | OPENSSL_ZERO_PADDING, $ary_iv);
    $pad_len = ord(substr($decrypted, -1));
    return substr($decrypted, 0, -$pad_len);
}

